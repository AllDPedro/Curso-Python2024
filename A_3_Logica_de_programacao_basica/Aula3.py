''' 
Python = Linguagem de programação
Tipo de tipagem = Dinâmica /  Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

'''

# ASPAS simples ' ' 
print('Estou usando ASPAS Simples para mostrar essa mensagem = ' ' ')

# ASPAS duplas " "
print("Estou usando ASPAS Duplas para mostrar essa mensagem = " " ")

# Escape 
print("Allan Delon Pedro \" da Silva Farias") # o Escape é o \ para não interpretar a aspas duplas que está do lado dele e sim mostrar ela.

# r 
print(r"Allan Delon Pedro \" da Silva Farias\"" ) # Colocando o r no começo antes das aspas duplas mostra tudo no código.

## Tem como colocar aspas duplas dentro das aspas simples e vice-versa. 


